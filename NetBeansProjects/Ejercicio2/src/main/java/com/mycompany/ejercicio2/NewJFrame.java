    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rb="";
        if(jRadioButton1.isSelected()){
            rb="Windows";
        }else if(jRadioButton2.isSelected()){
            rb="Linux";
        }else if(jRadioButton3.isSelected()){
            rb="Mac";
        }
        
        String esp1="";String esp2="";String esp3="";
        if(jCheckBox1.isSelected()){
            esp1 = " "+jCheckBox1.getText();
            //esp.concat(" "+jCheckBox1.getText());
        }if(jCheckBox2.isSelected()){
            esp2 = " "+jCheckBox2.getText();
            //esp.concat(" "+jCheckBox2.getText());
        }if(jCheckBox3.isSelected()){
            esp3 = " "+jCheckBox3.getText();
            //esp.concat(" "+jCheckBox3.getText());
        }
        
        Integer horas=(Integer)jSpinner1.getValue();
        
        JOptionPane.showMessageDialog(
                 null, "Sistema operativo: "+rb +"\nEspecializacion: "+esp1+esp2+esp3+"\nHoras: "+horas, "Resultado", JOptionPane.OK_OPTION);
                    
    }//GEN-LAST:event_jButton1ActionPerformed

